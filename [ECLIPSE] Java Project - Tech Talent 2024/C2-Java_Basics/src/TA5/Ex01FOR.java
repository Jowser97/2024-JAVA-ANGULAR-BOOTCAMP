package TA5;

public class Ex01FOR {
	
	public static void main(String[] args) {
		
		/*Ejercicio: escribe un programa que imprima toda la tabla de multiplicar del 5
		(desde 0 hasta 10). Con metodo for*/
		
		for (int n = 0; n <= 10; n++){
			System.out.println("5 * " + n + " = " + (5*n));
			
		}
	}
}
