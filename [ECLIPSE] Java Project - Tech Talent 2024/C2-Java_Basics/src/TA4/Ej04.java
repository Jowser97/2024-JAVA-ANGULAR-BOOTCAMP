package TA4;

public class Ej04 {

	public static void main(String[] args) {
		/*Escribe un programa Java que declare una variable entera N y asignale
		un valor. A contiunacion escribe las instrucciones que realicen los siguientes*/

		int n = 5;
		
		/*Incrementar N en 77
		Decrementar en 3
		Duplicar si valor*/
		
		//Al incrementar N en 77 la varibla pasa a tener un valor de 82
		n += 77;
		System.out.println(n);
		
		//Al decrementar N en 3 la variable pasa a tener un valor de 79
		n -= 3;
		System.out.println(n);
		
		//Al duplicar el valor de N la variable pasa a tener un valor de 158
		n *= 2;
		System.out.println(n);
		
		//En consola se imprimira
		//82
		//79
		//158
		
		/*Hay que tener en cuenta que el valor de N segun el orden de las
		de las modificaciones*/
		
		/*En este ejercicio he aprendido a realizar operaciones a una variable
		y modificarla*/
		
	}

}
